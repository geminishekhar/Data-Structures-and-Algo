package operators;

public class BitwiseOperators {

	public static void main(String[] args) {
		int a=5;
		int b=6;
		int e=13;
		int c= a & b;
		int d= a | b;
		int right_shift= e >> 2;
		int left_shift =e << 2;
		System.out.println(c);
		System.out.println(d);
		System.out.println(right_shift);
		System.out.println(left_shift);
		

	}

}
